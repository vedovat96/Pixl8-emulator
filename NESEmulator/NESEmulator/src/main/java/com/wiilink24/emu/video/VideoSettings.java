package com.wiilink24.emu.video;

public class VideoSettings {
    public static int width;
    public int length;
    public void setWidth(int width){
        this.width = width;
    }
    public int getWidth(){
        return width;
    }
    public void setLength(int length){
        this.length = length;
    }
    public int getLength() {
        return length;
    }
}
